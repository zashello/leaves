static void ssd1306WriteCommand(uint8_t cmd)
{
    i2cSoftStart();
    i2cSoftSendByte(g_config.address << 1);
    i2cSoftWaitAck();
    i2cSoftSendByte(0x00);
    i2cSoftWaitAck();
    i2cSoftSendByte(cmd);
    i2cSoftWaitAck();
    i2cSoftStop();
}

static void ssd1306WriteData(uint8_t *data, uint16_t len)
{
    if (data == NULL || len == 0) return;

    i2cSoftStart();
    i2cSoftSendByte(g_config.address << 1);
    i2cSoftWaitAck();
    i2cSoftSendByte(0x40);
    i2cSoftWaitAck();

    for (uint16_t i = 0; i < len; i++) {
        i2cSoftSendByte(data[i]);
        i2cSoftWaitAck();
    }

    i2cSoftStop();
}

esp_err_t ssd1306Init(const ssd1306_config_t *config)
{
    if (config == NULL) {
        return ESP_ERR_INVALID_ARG;
    }

    if (g_initialized) {
        ESP_LOGW(TAG, "SSD1306已初始化");
        return ESP_OK;
    }

    memcpy(&g_config, config, sizeof(ssd1306_state_t));

    ESP_LOGI(TAG, "初始化SSD1306: 地址=0x%02X, SCL=GPIO%d, SDA=GPIO%d",
             g_config.address, g_config.sclPin, g_config.sdaPin);

    esp_err_t ret = i2cSoftInit(g_config.sclPin, g_config.sdaPin);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "软件I2C初始化失败");
        return ret;
    }

    i2cSoftStart();
    i2cSoftSendByte(g_config.address << 1);
    bool ack = i2cSoftWaitAck();
    i2cSoftStop();

    if (!ack) {
        ESP_LOGW(TAG, "OLED未响应，继续尝试初始化");
    }

    ssd1306WriteCommand(0xAE);
    ssd1306WriteCommand(0xD5);
    ssd1306WriteCommand(0x80);
    ssd1306WriteCommand(0xA8);
    ssd1306WriteCommand(0x3F);
    ssd1306WriteCommand(0xD3);
    ssd1306WriteCommand(0x00);
    ssd1306WriteCommand(0x40);
    ssd1306WriteCommand(0x8D);
    ssd1306WriteCommand(0x14);
    ssd1306WriteCommand(0x20);
    ssd1306WriteCommand(0x00);
    ssd1306WriteCommand(0xA1);
    ssd1306WriteCommand(0xC0);
    ssd1306WriteCommand(0xDA);
    ssd1306WriteCommand(0x12);
    ssd1306WriteCommand(0x81);
    ssd1306WriteCommand(0xCF);
    ssd1306WriteCommand(0xD9);
    ssd1306WriteCommand(0xF1);
    ssd1306WriteCommand(0xDB);
    ssd1306WriteCommand(0x40);
    ssd1306WriteCommand(0xA4);
    ssd1306WriteCommand(0xA6);
    ssd1306WriteCommand(0x2E);
    ssd1306WriteCommand(0xAF);

    memset(g_buffer, 0, SSD1306_BUFFER_SIZE);
    g_cursorX = 0;
    g_cursorY = 0;

    g_initialized = true;
    ESP_LOGI(TAG, "SSD1306初始化成功");
    return ESP_OK;
}

void ssd1306Deinit(void)
{
    if (!g_initialized) return;

    i2cSoftDeinit();
    g_initialized = false;
    ESP_LOGI(TAG, "SSD1306已释放");
}

void ssd1306Clear(void)
{
    if (!g_initialized) return;

    memset(g_buffer, 0, SSD1306_BUFFER_SIZE);
    g_cursorX = 0;
    g_cursorY = 0;
}

void ssd1306Display(void)
{
    if (!g_initialized) return;

    ssd1306WriteCommand(0x21);
    ssd1306WriteCommand(g_config.columnOffset);
    ssd1306WriteCommand(127);
    ssd1306WriteCommand(0x22);
    ssd1306WriteCommand(g_config.pageOffset);
    ssd1306WriteCommand(7);

    uint8_t displayData[128 * 8];
    memset(displayData, 0, sizeof(displayData));

    for (int page = 0; page < 8; page++) {
        int bufferStartOffset = (page - g_config.pageOffset) * 128;
        if (bufferStartOffset >= 0 && bufferStartOffset < SSD1306_BUFFER_SIZE) {
            int copyLen = 127 - g_config.columnOffset + 1;
            if (copyLen > 128) copyLen = 128;
            if (bufferStartOffset + copyLen <= SSD1306_BUFFER_SIZE) {
                memcpy(displayData + page * 128, g_buffer + bufferStartOffset + g_config.columnOffset, copyLen);
            }
        }
    }

    ssd1306WriteData(displayData, 128 * (8 - g_config.pageOffset));
}

void ssd1306DrawPixel(int16_t x, int16_t y, uint8_t color)
{
    if (!g_initialized) return;

    if (x < g_config.columnOffset || y < g_config.pageOffset * 8 ||
        x >= g_config.width || y >= g_config.height) {
        return;
    }

    int16_t bufX = x - g_config.columnOffset;
    int16_t bufY = (y / 8) - g_config.pageOffset;
    uint16_t index = bufY * 128 + bufX;

    if (index >= SSD1306_BUFFER_SIZE) return;

    uint8_t bitMask = 1 << (y & 7);

    if (color == SSD1306_WHITE) {
        g_buffer[index] |= bitMask;
    } else {
        g_buffer[index] &= ~bitMask;
    }
}

void ssd1306SetCursor(int16_t x, int16_t y)
{
    if (x < 0) x = 0;
    if (y < 0) y = 0;
    g_cursorX = x;
    g_cursorY = 55 - y;
}

void ssd1306Write(char c)
{
    if (!g_initialized) return;

    if (c < 32 || c > 126) {
        c = ' ';
    }

    const uint8_t *charBitmap = font5x7[c - 32];

    for (int i = 0; i < 5; i++) {
        uint8_t colData = charBitmap[i];
        for (int j = 0; j < 8; j++) {
            if (colData & (1 << (7 - j))) {
                ssd1306DrawPixel(g_cursorX + i, g_cursorY + j, SSD1306_WHITE);
            }
        }
    }

    g_cursorX += 6;
}

void ssd1306Print(const char *text)
{
    if (text == NULL) return;

    while (*text) {
        ssd1306Write(*text);
        text++;
    }
}

void ssd1306PrintFloat(float value, uint8_t decimals)
{
    if (!g_initialized) return;

    char str[20];
    snprintf(str, sizeof(str), "%.*f", decimals, value);
    ssd1306Print(str);
}
